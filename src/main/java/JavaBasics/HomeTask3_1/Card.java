package JavaBasics.HomeTask3_1;

public class Card {

    String runck;
    String suit;

    public Card(String runck, String suit){

        this.runck = runck;
        this.suit = suit;
    }

    public String getRunck(){
        return this.runck;
    }

    public String getSuit(){
        return this.suit;
    }

}


