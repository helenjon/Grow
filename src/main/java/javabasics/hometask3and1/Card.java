package javabasics.hometask3and1;

public class Card {

    final String rank;
    final String suit;

    public Card(String rank, String suit) {
        this.rank = rank;
        this.suit = suit;
    }

    public String getRunck() {
        return this.rank;
    }

    public String getSuit() {
        return this.rank;
    }
}


